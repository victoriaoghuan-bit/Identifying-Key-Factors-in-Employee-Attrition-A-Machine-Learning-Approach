# Load dataset
df = pd.read_csv("student_data.csv")

# Step 1: Handle Missing Values (Drop columns missing more than 5%)
missing_threshold = 0.05 * len(df)
df = df.dropna(thresh=len(df) - missing_threshold, axis=1)  # Drop columns with excessive missing values

# Fill missing values appropriately
for col in df.columns:
    if df[col].dtype == "object":  # Categorical
        df[col].fillna(df[col].mode()[0], inplace=True)
    else:  # Numeric
        df[col].fillna(df[col].median(), inplace=True)

# Step 2: Encode Categorical Variables
categorical_cols = df.select_dtypes(include=['object']).columns
for col in categorical_cols:
    if df[col].nunique() <= 10:
        # Create a temporary DataFrame with the one-hot encoded columns
        temp_df = pd.get_dummies(df[col], prefix=col, drop_first=True)
        # Concatenate the temporary DataFrame with the original DataFrame
        df = pd.concat([df, temp_df], axis=1)
        # Drop the original categorical column
        df = df.drop(columns=[col])
    else:
        le = LabelEncoder()
        df[col] = le.fit_transform(df[col])

# Step 3: Remove Duplicate Entries
df.drop_duplicates(inplace=True)

# Step 4: Handle Outliers using Z-Score Method
# Select only numeric columns for outlier handling
